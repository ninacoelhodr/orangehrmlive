import 'cypress-file-upload';

// For this to work the file has to be in the fixtures folder
Cypress.Commands.add('upload', (locator, fileName) => {  
  cy.fixture(fileName).then(fileContent => {
    cy.get(locator).attachFile({ fileContent, fileName, mimeType: 'application/pdf' }, { subjectType: 'drag-n-drop' });
  });
})
